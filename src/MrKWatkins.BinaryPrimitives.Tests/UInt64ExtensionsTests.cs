namespace MrKWatkins.BinaryPrimitives.Tests;

public sealed class UInt64ExtensionsTests
{
    [Test]
    public void GetUInt64_Array()
    {
        byte[] bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B];

        bytes.GetUInt64(1).Should().Equal(0x0908070605040302UL);
    }

    [Test]
    public void GetUInt64_Array_Endian()
    {
        byte[] bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B];

        bytes.GetUInt64(1, Endian.Little).Should().Equal(0x0908070605040302UL);
        bytes.GetUInt64(2, Endian.Big).Should().Equal(0x030405060708090AUL);
    }

    [Test]
    public void GetUInt64_ReadOnlySpan()
    {
        ReadOnlySpan<byte> bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];

        bytes.GetUInt64().Should().Equal(0x0807060504030201UL);
    }

    [Test]
    public void GetUInt64_ReadOnlySpan_Endian()
    {
        ReadOnlySpan<byte> bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];

        bytes.GetUInt64(Endian.Little).Should().Equal(0x0807060504030201UL);
        bytes.GetUInt64(Endian.Big).Should().Equal(0x0102030405060708UL);
    }

    [Test]
