namespace MrKWatkins.BinaryPrimitives.Tests;

public sealed class Int64ExtensionsTests
{
    [Test]
    public void GetInt64_Array()
    {
        byte[] bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B];

        bytes.GetInt64(1).Should().Equal(0x0908070605040302L);
    }

    [Test]
    public void GetInt64_Array_Endian()
    {
        byte[] bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B];

        bytes.GetInt64(1, Endian.Little).Should().Equal(0x0908070605040302L);
        bytes.GetInt64(2, Endian.Big).Should().Equal(0x030405060708090AL);
    }

    [Test]
    public void GetInt64_ReadOnlySpan()
    {
        ReadOnlySpan<byte> bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];

        bytes.GetInt64().Should().Equal(0x0807060504030201L);
    }

    [Test]
    public void GetInt64_ReadOnlySpan_Endian()
    {
        ReadOnlySpan<byte> bytes = [0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08];

        bytes.GetInt64(Endian.Little).Should().Equal(0x0807060504030201L);
        bytes.GetInt64(Endian.Big).Should().Equal(0x0102030405060708L);
    }

