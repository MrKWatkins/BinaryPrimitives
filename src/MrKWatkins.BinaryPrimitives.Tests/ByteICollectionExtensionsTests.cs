namespace MrKWatkins.BinaryPrimitives.Tests;

public sealed class ByteICollectionExtensionsTests
{
    [Test]
    public void AddUInt16()
    {
        List<byte> bytes = [0x01];

        bytes.AddUInt16(0x1234);
        bytes.Should().SequenceEqual(0x01, 0x34, 0x12);

        bytes.AddUInt16(0x5678, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0x34, 0x12, 0x56, 0x78);
    }

    [Test]
    public void AddInt16()
    {
        List<byte> bytes = [0x01];

        bytes.AddInt16(0x1234);
        bytes.Should().SequenceEqual(0x01, 0x34, 0x12);

        bytes.AddInt16(0x5678, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0x34, 0x12, 0x56, 0x78);
    }

    [Test]
    public void AddUInt24()
    {
        List<byte> bytes = [0x01];

        bytes.AddUInt24(0x123456);
        bytes.Should().SequenceEqual(0x01, 0x56, 0x34, 0x12);

        bytes.AddUInt24(0x789ABC, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0x56, 0x34, 0x12, 0x78, 0x9A, 0xBC);
    }

    [Test]
    public void AddInt32()
    {
        List<byte> bytes = [0x01];

        bytes.AddInt32(0x12345678);
        bytes.Should().SequenceEqual(0x01, 0x78, 0x56, 0x34, 0x12);

        bytes.AddInt32(0x12345678, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0x78, 0x56, 0x34, 0x12, 0x12, 0x34, 0x56, 0x78);
    }

    [Test]
    public void AddUInt32()
    {
        List<byte> bytes = [0x01];

        bytes.AddUInt32(0x12345678U);
        bytes.Should().SequenceEqual(0x01, 0x78, 0x56, 0x34, 0x12);

        bytes.AddUInt32(0x12345678U, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0x78, 0x56, 0x34, 0x12, 0x12, 0x34, 0x56, 0x78);
    }

    [Test]
    public void AddInt64()
    {
        List<byte> bytes = [0x01];

        bytes.AddInt64(0x1234567890ABCDEF);
        bytes.Should().SequenceEqual(0x01, 0xEF, 0xCD, 0xAB, 0x90, 0x78, 0x56, 0x34, 0x12);

        bytes.AddInt64(0x1234567890ABCDEF, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0xEF, 0xCD, 0xAB, 0x90, 0x78, 0x56, 0x34, 0x12, 0x12, 0x34, 0x56, 0x78, 0x90, 0xAB, 0xCD, 0xEF);
    }

    [Test]
    public void AddUInt64()
    {
        List<byte> bytes = [0x01];

        bytes.AddUInt64(0x1234567890ABCDEFul);
        bytes.Should().SequenceEqual(0x01, 0xEF, 0xCD, 0xAB, 0x90, 0x78, 0x56, 0x34, 0x12);

        bytes.AddUInt64(0x1234567890ABCDEFUL, Endian.Big);
        bytes.Should().SequenceEqual(0x01, 0xEF, 0xCD, 0xAB, 0x90, 0x78, 0x56, 0x34, 0x12, 0x12, 0x34, 0x56, 0x78, 0x90, 0xAB, 0xCD, 0xEF);
    }
}